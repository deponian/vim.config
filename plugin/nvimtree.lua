local ripgrep = function (node)
  local dir = ""
  if node.fs_stat.type == "directory" then
    dir = vim.fn.fnamemodify(node.absolute_path, ":.")
  else
    dir = vim.fn.fnamemodify(node.absolute_path, ":.:h")
  end
  vim.cmd('RG ' .. dir)
end

local ripgrepnohidden = function (node)
  local dir = ""
  if node.fs_stat.type == "directory" then
    dir = vim.fn.fnamemodify(node.absolute_path, ":.")
  else
    dir = vim.fn.fnamemodify(node.absolute_path, ":.:h")
  end
  vim.cmd('RGNH ' .. dir)
end

